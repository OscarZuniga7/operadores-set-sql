-- Schema mirrors the SQLite version (be_channels.sqlite).

DROP TABLE IF EXISTS app_clients;
DROP TABLE IF EXISTS web_clients;
DROP TABLE IF EXISTS atm_clients;
DROP TABLE IF EXISTS clients;

CREATE TABLE clients(
  id INT PRIMARY KEY,
  rut VARCHAR(20) UNIQUE NOT NULL,
  nombre VARCHAR(100) NOT NULL
) ENGINE=InnoDB;

CREATE TABLE web_clients(
  client_id INT NOT NULL PRIMARY KEY,
  CONSTRAINT fk_web_client FOREIGN KEY (client_id) REFERENCES clients(id) ON DELETE CASCADE
) ENGINE=InnoDB;

CREATE TABLE app_clients(
  client_id INT NOT NULL PRIMARY KEY,
  CONSTRAINT fk_app_client FOREIGN KEY (client_id) REFERENCES clients(id) ON DELETE CASCADE
) ENGINE=InnoDB;

CREATE TABLE atm_clients(
  client_id INT NOT NULL PRIMARY KEY,
  CONSTRAINT fk_atm_client FOREIGN KEY (client_id) REFERENCES clients(id) ON DELETE CASCADE
) ENGINE=InnoDB;

